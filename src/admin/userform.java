    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    int rowIndex = table_name.getSelectedRow();
    if(rowIndex <0){
        JOptionPane.showMessageDialog(null, "Please select an Item!");
    }else{
        try{
            dbConnector dbc = new dbConnector();
            TableModel tbl =  table_name.getModel();
            ResultSet rs = dbc.getData("SELECT *FROM tbl_user WHERE id ='"+tbl.getValueAt(rowIndex, 0)+"'");
            if(rs.next()){
                createuserform cuf = new createuserform();
                  cuf.uid.setText(""+rs.getInt("id"));
                cuf.fn.setText(""+rs.getString("fn"));
                  cuf.mdn.setText(""+rs.getString("middle"));
                    cuf.ln.setText(""+rs.getString("ln"));
                      cuf.un.setText(""+rs.getString("username"));
                      cuf.Add.setEnabled(false);
                      cuf.update.setEnabled(true);
                      cuf.image.setIcon(cuf.ResizeImage(rs.getString("image"), null, cuf.image));
                      cuf.oldpath = rs.getString("image");
                      cuf.path =  rs.getString("image");
                      cuf.destination =  rs.getString("image");
                       cuf.select.setEnabled(false);
                        cuf.remove.setEnabled(true);
                      cuf.setVisible(true);
                      
                      if( rs.getString("image").isEmpty()){
                          cuf.select.setEnabled(true);
                        cuf.remove.setEnabled(false);
                      }else{
                          cuf.select.setEnabled(false);
                        cuf.remove.setEnabled(true);
                      }
                      this.dispose();
            }
            
            
            
        }catch(SQLException ex){
            System.out.println(""+ex);
            
        }
    }
    }//GEN-LAST:event_jButton2ActionPerformed

